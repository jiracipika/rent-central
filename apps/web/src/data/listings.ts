export function getListingsByCity(city: string): Property[] {
  return listings.filter((l) => l.city.toLowerCase() === city.toLowerCase());
}

export function getListingById(id: string): Property | undefined {
  return listings.find((l) => l.id === id);
}

export function getSimilarListings(listing: Property, limit = 3): Property[] {
  return listings
    .filter((l) => l.id !== listing.id)
    .sort((a, b) => {
      // Prefer same city, then similar type, then similar price
      const aScore = (a.city === listing.city ? 2 : 0) + (a.type === listing.type ? 1 : 0);
      const bScore = (b.city === listing.city ? 2 : 0) + (b.type === listing.type ? 1 : 0);
      return bScore - aScore;
    })
    .slice(0, limit);
}
